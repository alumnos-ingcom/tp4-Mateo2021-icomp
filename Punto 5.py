# Numeros Positivos y Negativos

def prueba():
    print("Bienvenido")
    numero = int(input("Ingrese un numero: "))
    numerodos = 0
    if numero == numerodos :
        print("El numero es igual a 0")
    if numero > numerodos :
        print("El numero es positivo")
    if numero < numerodos :
        print("El numero es negativo")

if __name__ == "__main__":
    prueba()