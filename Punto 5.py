# Numeros Positivos y Negativos
print("Bienvenido, a continuacion ingrese un valor")
numero = int(input())
numerodos = 0

if numero == numerodos :
    print("El numero es igual a 0")
    
if numero > numerodos :
    print("El numero es positivo")
    
if numero < numerodos :
    print("El numero es negativo")
    